package com.giftservice.enums;

/**
 * Event and occasion-based categorization for gift suggestions.
 * This enum defines various occasions and events that typically
 * involve gift-giving to help match appropriate gifts for the context.
 */
public enum Occasion {
    
    /**
     * Birthday celebrations
     */
    BIRTHDAY,
    
    /**
     * Wedding ceremonies and celebrations
     */
    WEDDING,
    
    /**
     * Wedding anniversaries
     */
    ANNIVERSARY,
    
    /**
     * Graduation ceremonies
     */
    GRADUATION,
    
    /**
     * Christmas holiday
     */
    CHRISTMAS,
    
    /**
     * Valentine's Day
     */
    VALENTINES_DAY,
    
    /**
     * Mother's Day
     */
    MOTHERS_DAY,
    
    /**
     * Father's Day
     */
    FATHERS_DAY,
    
    /**
     * Easter holiday
     */
    EASTER,
    
    /**
     * New Year celebrations
     */
    NEW_YEAR,
    
    /**
     * Thanksgiving holiday
     */
    THANKSGIVING,
    
    /**
     * Baby shower celebrations
     */
    BABY_SHOWER,
    
    /**
     * Bridal shower celebrations
     */
    BRIDAL_SHOWER,
    
    /**
     * Housewarming parties
     */
    HOUSEWARMING,
    
    /**
     * Retirement celebrations
     */
    RETIREMENT,
    
    /**
     * Get well soon gifts
     */
    GET_WELL,
    
    /**
     * Thank you gifts
     */
    THANK_YOU,
    
    /**
     * Just because - no specific occasion
     */
    JUST_BECAUSE
}